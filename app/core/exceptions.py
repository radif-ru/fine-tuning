"""Иерархия исключений для фреймворка fine-tuning.

Все исключения наследуются от FineTuningError для унифицированной обработки ошибок.
"""


class FineTuningError(Exception):
    """Базовый класс для всех ошибок фреймворка."""
    
    def __init__(self, message: str, **kwargs):
        """Инициализация ошибки с сообщением и дополнительным контекстом.
        
        Args:
            message: Описание ошибки
            **kwargs: Дополнительный контекст (будет добавлен к сообщению)
        """
        if kwargs:
            context = " | ".join(f"{k}={v}" for k, v in kwargs.items())
            message = f"{message} | {context}"
        super().__init__(message)


class ConfigurationError(FineTuningError):
    """Ошибка в конфигурации приложения.
    
    Возникает при:
    - Отсутствии обязательных полей в .env
    - Невалидных значениях параметров
    - Ошибках парсинга конфигурации
    """
    pass


class ModelLoadError(FineTuningError):
    """Ошибка загрузки модели.
    
    Возникает при:
    - Недоступности HuggingFace Hub
    - Неверном имени модели
    - Ошибках загрузки весов
    - Нехватке памяти для загрузки
    """
    pass


class LoRAError(FineTuningError):
    """Ошибка при работе с LoRA.
    
    Возникает при:
    - Ошибках применения LoRA к модели
    - Ошибках сохранения/загрузки адаптеров
    - Неверной конфигурации LoRA
    """
    pass


class DataLoadError(FineTuningError):
    """Ошибка загрузки данных.
    
    Возникает при:
    - Отсутствии файла данных
    - Невалидном формате данных
    - Ошибках парсинга JSON/CSV
    - Отсутствии обязательных колонок
    """
    pass


class DataFormatError(FineTuningError):
    """Ошибка форматирования данных.
    
    Возникает при:
    - Неправильном формате инструкций
    - Ошибках токенизации
    - Несоответствии шаблонов данных
    """
    pass


class TrainingError(FineTuningError):
    """Ошибка во время обучения.
    
    Возникает при:
    - OOM (Out of Memory) во время тренировки
    - Ошибках в тренировочном цикле
    - Невозможности сохранить чекпоинт
    """
    pass


class InferenceError(FineTuningError):
    """Ошибка во время инференса.
    
    Возникает при:
    - Ошибках генерации текста
    - Недоступности модели для инференса
    - Невалидных параметрах генерации
    """
    pass


class CheckpointError(FineTuningError):
    """Ошибка работы с чекпоинтами.
    
    Возникает при:
    - Ошибках сохранения чекпоинта
    - Ошибках загрузки чекпоинта
    - Невозможности resume from checkpoint
    """
    pass


class ExportError(FineTuningError):
    """Ошибка экспорта модели.
    
    Возникает при:
    - Ошибках merge LoRA адаптера
    - Ошибках сохранения объединённой модели
    - Нехватке дискового пространства
    """
    pass
